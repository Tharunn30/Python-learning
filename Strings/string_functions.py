str = "i am a coder"

# 1. str.endswith("er"). It says true or false
print(str.endswith("er"))
print(str.endswith("ar"))

#2. str.Capitalize(). Makes the first letter captial
print(str.capitalize())

#3. str.replace(old,new). Replaces old value with new value
print(str.replace("a", "t"))

#4. str.find(word). It returns 1st index of 1st occurrer
print(str.find("a"))

#5. str.count("am"). It counts the occurance
print(str.count("am")) 