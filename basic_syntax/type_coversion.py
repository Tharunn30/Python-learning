# There are 2 methods to convert data types, one is type coversion and another is type casting

# Type Conversion:
# Type conversion is a process of changing the data type of a variable. It is a safe process
# type converstion automatically converts the data type on its own. 
a = 2
b = 4.24
sum = a + b
print(sum)  # Output: 6.24


#type casting:
# Type casting is a process of changing the data type of a variable. It is a manual process
# type casting requires explicit conversion of data type. It is also known as explicit type conversion.

c = "2"
d = 10
print(int(c) + d)  # Output: 12