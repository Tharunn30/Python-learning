# arithmetic operators
a = 5
b = 2
sum = a + b
print(sum) 
print(a + b)
print(a - b)
print(a * b)
print(a / b)

#modulo operator
print(a % b) #reminder between two numbers

#power operator
print(a ** b) #a to the power of b


# comparison / relation operators ( ==, !=, >, <, >=, <= )
c = 50
d = 20

print(c == d) #gives output only in true or false
print(c != d)
print(c > d)
print(c < d)
print(c >= d)
print(c <= d)


#Assignment operators (=, +=, *=, /=, %=, **=)
num= 10
num = 10 + num
num += 10 #(same as previous step)
print(num)


